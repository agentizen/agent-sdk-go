func main() {
	capabilitiesPath := flag.String("capabilities", "pkg/model/capabilities.go", "Path to capabilities.go")
	pricingPath := flag.String("pricing", "pkg/model/pricing.go", "Path to pricing.go")
	timeoutSec := flag.Int("timeout", 30, "HTTP timeout in seconds")
	writeChanges := flag.Bool("write", false, "Write changes to output files (default: report only)")
	verbose := flag.Bool("verbose", false, "Print discovered model updates per provider")
	flag.Parse()

	if err := run(*capabilitiesPath, *pricingPath, *timeoutSec, *writeChanges, *verbose); err != nil {
		fmt.Fprintf(os.Stderr, "%v\n", err)
		os.Exit(1)
	}
}

func run(capabilitiesPath, pricingPath string, timeoutSec int, writeChanges, verbose bool) error {
	capsFileBytes, err := os.ReadFile(capabilitiesPath)
	if err != nil {
		return fmt.Errorf("read capabilities file: %w", err)
	}
	pricingFileBytes, err := os.ReadFile(pricingPath)
	if err != nil {
		return fmt.Errorf("read pricing file: %w", err)
	}

	httpClient := &http.Client{Timeout: timeDurationSeconds(timeoutSec)}

	capUpdates, err := collectCapabilityUpdates(httpClient)
	if err != nil {
		return err
	}
	priceUpdates, err := collectPricingUpdates(httpClient)
	if err != nil {
		return err
	}

	if !hasNewCapabilityUpdates(capUpdates) && !hasNewPricingUpdates(priceUpdates) {
		fmt.Println("model registry files are already aligned with official provider docs")
		return nil
	}

	reportCapabilityUpdates(capUpdates, verbose)
	reportPricingUpdates(priceUpdates, verbose)

	if !writeChanges {
		fmt.Println("report-only mode: re-run with -write to update registry files")
		return nil
	}

	updatedCapabilities, err := applyCapabilityUpdates(string(capsFileBytes), capUpdates)
	if err != nil {
		return fmt.Errorf("update capabilities file content: %w", err)
	}
	updatedCapabilities = ensureGeneratedHeader(updatedCapabilities)
	if err := os.WriteFile(capabilitiesPath, []byte(updatedCapabilities), 0644); err != nil {
		return fmt.Errorf("write capabilities file: %w", err)
	}

	updatedPricing, err := applyPricingUpdates(string(pricingFileBytes), priceUpdates)
	if err != nil {
		return fmt.Errorf("update pricing file content: %w", err)
	}
	updatedPricing = ensureGeneratedHeader(updatedPricing)
	if err := os.WriteFile(pricingPath, []byte(updatedPricing), 0644); err != nil {
		return fmt.Errorf("write pricing file: %w", err)
	}

	fmt.Println("updated capabilities.go and pricing.go from official provider sources")
	return nil
}

func collectCapabilityUpdates(client *http.Client) (capabilityUpdates, error) {
	collector := newCapabilityCollector()
	for _, src := range capabilitySources {
		sourceURL, ok := model.OfficialProviderModelDocsURL(src.providerID)
		if !ok || strings.TrimSpace(sourceURL) == "" {
			return capabilityUpdates{}, fmt.Errorf("missing official docs URL for provider: %s", src.providerID)
		}

		content, err := fetchProviderModelsCorpus(client, src.providerID, sourceURL)
		if err != nil {
			return capabilityUpdates{}, fmt.Errorf("fetch %s models page failed: %w", src.providerID, err)
		}

		modelIDs := extractModelIDs(content, src.prefixes, src.allow, normalizeRules{
			allowNoDigitAliases: knownNoDigitModelAliases,
			rejectNoiseTokens:   true,
		})
		for _, modelID := range modelIDs {
			if model.ProviderSupports(src.providerID, modelID, model.CapabilityVision) ||
				model.ProviderSupports(src.providerID, modelID, model.CapabilityDocuments) {
				continue
			}
			inferred := src.infer(modelID)
			if !inferred.vision && !inferred.documents {
				continue
			}
			collector.add(src.providerID, modelID, inferred)
		}
	}
	return collector.finalize(), nil
}

func collectPricingUpdates(client *http.Client) (pricingUpdates, error) {
	collector := newPricingCollector()

	type providerFetcher struct {
		id      string
		extract func(*http.Client, string) (map[string]pricingSet, error)
	}
	fetchers := []providerFetcher{
		{"openai", extractOpenAIPricing},
		{"anthropic", extractAnthropicPricing},
		{"gemini", extractGeminiPricing},
		{"mistral", extractMistralPricing},
	}

	for _, f := range fetchers {
		collector.touch(f.id)

		pricingURL, ok := model.OfficialProviderPricingURL(f.id)
		if !ok || strings.TrimSpace(pricingURL) == "" {
			return pricingUpdates{}, fmt.Errorf("missing pricing URL for provider: %s", f.id)
		}

		scraped, err := f.extract(client, pricingURL)
		if err != nil {
			fmt.Fprintf(os.Stderr, "warning: skip pricing sync for provider=%s: %v\n", f.id, err)
			continue
		}

		for modelID, ps := range scraped {
			collector.add(f.id, modelID, ps)
		}
	}
	return collector.finalize(), nil
}

func hasNewCapabilityUpdates(updates capabilityUpdates) bool {
	for _, provider := range updates.providers {
		if len(provider.models) > 0 {
			return true
		}
	}
	return false
}

func hasNewPricingUpdates(updates pricingUpdates) bool {
	for _, provider := range updates.providers {
		if len(provider.models) > 0 {
			return true
		}
	}
	return false
}

func fetchProviderPricingCorpus(client *http.Client, providerID, primaryURL string) (string, error) {
	urls := pricingURLCandidates(providerID, primaryURL)
	var errs []string
	for _, url := range urls {
		content, err := fetchDocumentCorpus(client, url, "agent-sdk-go-model-registry-sync/1.0", corpusOptions{})
		if err == nil {
			return content, nil
		}
		errs = append(errs, fmt.Sprintf("%s (%v)", url, err))
	}
	return "", fmt.Errorf("all pricing URL candidates failed: %s", strings.Join(errs, "; "))
}

func fetchProviderModelsCorpus(client *http.Client, providerID, primaryURL string) (string, error) {
	urls := modelURLCandidates(providerID, primaryURL)
	var errs []string
	for _, candidateURL := range urls {
		if providerID == "openai" {
			content, err := fetchOpenAIModelsCorpus(client, candidateURL)
			if err == nil {
				return content, nil
			}
			errs = append(errs, fmt.Sprintf("%s (%v)", candidateURL, err))
			continue
		}

		content, err := fetchDocumentCorpus(client, candidateURL, "agent-sdk-go-model-registry-sync/1.0", corpusOptions{includeLinks: true})
		if err == nil {
			return content, nil
		}
		errs = append(errs, fmt.Sprintf("%s (%v)", candidateURL, err))
	}
	return "", fmt.Errorf("all models URL candidates failed: %s", strings.Join(errs, "; "))
}

func fetchOpenAIModelsCorpus(client *http.Client, entryURL string) (string, error) {
	body, err := fetchDocumentRaw(client, entryURL, "agent-sdk-go-model-registry-sync/1.0")
	if err != nil {
		return "", err
	}

	corpus, links, err := extractDocumentCorpusAndLinks(body, corpusOptions{includeLinks: true})
	if err != nil {
		return "", err
	}

	modelLinks := filterOpenAIModelLinks(entryURL, links)
	if len(modelLinks) == 0 {
		return corpus, nil
	}

	var b strings.Builder
	b.WriteString(corpus)
	b.WriteString(" ")

	maxPages := 20
	for i, link := range modelLinks {
		if i >= maxPages {
			break
		}
		subCorpus, fetchErr := fetchDocumentCorpus(client, link, "agent-sdk-go-model-registry-sync/1.0", corpusOptions{})
		if fetchErr != nil {
			continue
		}
		b.WriteString(subCorpus)
		b.WriteString(" ")
	}

	return spaceRegexp.ReplaceAllString(strings.ToLower(b.String()), " "), nil
}

func filterOpenAIModelLinks(baseURL string, hrefs []string) []string {
	resolved := map[string]struct{}{}
	out := make([]string, 0, len(hrefs))
	for _, href := range hrefs {
		abs, err := resolveAbsoluteURL(baseURL, href)
		if err != nil {
			continue
		}
		u, err := url.Parse(abs)
		if err != nil {
			continue
		}
		u.Fragment = ""
		if !strings.EqualFold(u.Host, "developers.openai.com") {
			continue
		}
		if !strings.HasPrefix(u.Path, "/api/docs/models") {
			continue
		}
		canonical := u.String()
		if _, ok := resolved[canonical]; ok {
			continue
		}
		resolved[canonical] = struct{}{}
		out = append(out, canonical)
	}
	sort.Strings(out)
	return out
}

func pricingURLCandidates(providerID, primaryURL string) []string {
	urls := []string{primaryURL}
	if providerID == "openai" {
		urls = append(urls,
			"https://developers.openai.com/api/docs/pricing",
		)
	}
	seen := map[string]struct{}{}
	out := make([]string, 0, len(urls))
	for _, url := range urls {
		trimmed := strings.TrimSpace(url)
		if trimmed == "" {
			continue
		}
		if _, ok := seen[trimmed]; ok {
			continue
		}
		seen[trimmed] = struct{}{}
		out = append(out, trimmed)
	}
	return out
}

