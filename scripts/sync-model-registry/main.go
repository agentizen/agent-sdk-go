// extractOpenAIPricing parses https://developers.openai.com/api/docs/pricing.
// The page has multiple HTML tables; we target tables whose header row contains
// Model, Input, and Output columns, skipping fine-tuning tables (have Training
// column) and non-token-priced rows (prices that contain "/").
func extractOpenAIPricing(client *http.Client, pageURL string) (map[string]pricingSet, error) {
	body, err := fetchDocumentRaw(client, pageURL, "agent-sdk-go-model-registry-sync/1.0")
	if err != nil {
		return nil, err
	}
	doc, err := goquery.NewDocumentFromReader(bytes.NewReader(body))
	if err != nil {
		return nil, err
	}

	out := map[string]pricingSet{}
	priorityByModel := map[string]int{}
	doc.Find("table").Each(func(_ int, tbl *goquery.Selection) {
		// Find the row that actually defines columns (the first row can be grouped colspans).
		var headers []string
		tbl.Find("tr").EachWithBreak(func(_ int, tr *goquery.Selection) bool {
			candidate := make([]string, 0)
			tr.Find("th,td").Each(func(_ int, c *goquery.Selection) {
				candidate = append(candidate, strings.ToLower(strings.TrimSpace(c.Text())))
			})
			for _, h := range candidate {
				if h == "model" {
					headers = candidate
					return false
				}
			}
			return true
		})
		if len(headers) == 0 {
			return
		}

		// Must have Model, Input, Output columns; must NOT have Training column.
		modelCol, inputCol, outputCol := -1, -1, -1
		hasTraining := false
		for i, h := range headers {
			switch {
			case h == "model":
				modelCol = i
			case strings.Contains(h, "input") && !strings.Contains(h, "cached"):
				if inputCol < 0 {
					inputCol = i
				}
			case h == "output":
				if outputCol < 0 {
					outputCol = i
				}
			case strings.Contains(h, "training"):
				hasTraining = true
			}
		}
		if hasTraining || modelCol < 0 || inputCol < 0 || outputCol < 0 {
			return
		}

		tablePriority := 0
		if pane := tbl.Closest("[data-content-switcher-pane]"); pane.Length() > 0 {
			switch strings.ToLower(strings.TrimSpace(pane.AttrOr("data-value", ""))) {
			case "standard":
				tablePriority = 2
			case "batch":
				tablePriority = 1
			}
		}

		tbl.Find("tr").Each(func(r int, row *goquery.Selection) {
			_ = r
			if row.Find("th").Length() > 0 {
				return
			}
			cells := row.Find("td")
			if cells.Length() <= outputCol {
				return
			}
			rawModel := strings.ToLower(strings.Join(strings.Fields(cells.Eq(modelCol).Text()), " "))
			modelID := strings.TrimSpace(strings.Split(rawModel, " ")[0])
			inputText := strings.TrimSpace(cells.Eq(inputCol).Text())
			outputText := strings.TrimSpace(cells.Eq(outputCol).Text())

			// Skip non-token-priced rows (e.g. "/ hour", "/ minute").
			if strings.Contains(inputText, "/") || strings.Contains(outputText, "/") {
				return
			}

			inCost := parseDollarAmount(inputText)
			outCost := parseDollarAmount(outputText)
			if inCost <= 0 && outCost <= 0 {
				return
			}
			if !allowOpenAIPricingID(modelID) {
				return
			}
			if prevPriority, exists := priorityByModel[modelID]; !exists || tablePriority > prevPriority {
				out[modelID] = pricingSet{inputCost: inCost, outputCost: outCost, found: true}
				priorityByModel[modelID] = tablePriority
			}
		})
	})
	return out, nil
}

// extractAnthropicPricing parses https://docs.anthropic.com/en/docs/about-claude/models.
// The page has a single feature table. We find the "Claude API ID" row for model
// identifiers and the "Pricing" row for costs formatted as "$X / input MTok$Y / output MTok".
func extractAnthropicPricing(client *http.Client, pageURL string) (map[string]pricingSet, error) {
	body, err := fetchDocumentRaw(client, pageURL, "agent-sdk-go-model-registry-sync/1.0")
	if err != nil {
		return nil, err
	}
	doc, err := goquery.NewDocumentFromReader(bytes.NewReader(body))
	if err != nil {
		return nil, err
	}

	out := map[string]pricingSet{}
	doc.Find("table").Each(func(_ int, tbl *goquery.Selection) {
		var modelIDs []string
		var pricingCells []string

		tbl.Find("tr").Each(func(_ int, row *goquery.Selection) {
			cells := row.Find("th,td")
			label := strings.ToLower(strings.TrimSpace(cells.First().Text()))
			switch {
			case strings.Contains(label, "api id") || strings.Contains(label, "api alias"):
				cells.Each(func(i int, td *goquery.Selection) {
					if i == 0 {
						return
					}
					modelIDs = append(modelIDs, strings.TrimSpace(td.Text()))
				})
			case label == "pricing" || strings.Contains(label, "pricing"):
				cells.Each(func(i int, td *goquery.Selection) {
					if i == 0 {
						return
					}
					pricingCells = append(pricingCells, strings.TrimSpace(td.Text()))
				})
			}
		})

		if len(modelIDs) == 0 || len(pricingCells) == 0 {
			return
		}
		for i, modelID := range modelIDs {
			if i >= len(pricingCells) {
				break
			}
			if !allowAnthropicPricingID(modelID) {
				continue
			}
			in, outCost := parseAnthropicPricingCell(pricingCells[i])
			if in <= 0 && outCost <= 0 {
				continue
			}
			if _, exists := out[modelID]; !exists {
				out[modelID] = pricingSet{inputCost: in, outputCost: outCost, found: true}
			}
		}
	})
	return out, nil
}

// parseAnthropicPricingCell parses cells like "$5 / input MTok$25 / output MTok".
func parseAnthropicPricingCell(cell string) (in, out float64) {
	matches := pricePattern.FindAllStringSubmatch(cell, -1)
	if len(matches) >= 1 {
		in, _ = strconv.ParseFloat(matches[0][1], 64)
	}
	if len(matches) >= 2 {
		out, _ = strconv.ParseFloat(matches[1][1], 64)
	}
	return
}

// extractGeminiPricing parses https://ai.google.dev/gemini-api/docs/pricing.
// The page uses <h2 id="gemini-*"> headings to identify models, each followed
// by a "Standard" section with a 3-column table [category | free | paid].
// We pick the first table after each h2 that has "Input price"/"Output price" rows.
func extractGeminiPricing(client *http.Client, pageURL string) (map[string]pricingSet, error) {
	body, err := fetchDocumentRaw(client, pageURL, "agent-sdk-go-model-registry-sync/1.0")
	if err != nil {
		return nil, err
	}
	doc, err := goquery.NewDocumentFromReader(bytes.NewReader(body))
	if err != nil {
		return nil, err
	}

	out := map[string]pricingSet{}

	// Walk through all h2 elements whose id starts with "gemini-".
	doc.Find("h2[id]").Each(func(_ int, h2 *goquery.Selection) {
		id, _ := h2.Attr("id")
		if !strings.HasPrefix(id, "gemini-") {
			return
		}
		modelID := id

		// Find the first <table> after this h2 that contains "Input price" rows.
		// We search siblings and descendants until the next h2.
		var ps pricingSet
		h2.NextUntil("h2").Find("table").EachWithBreak(func(_ int, tbl *goquery.Selection) bool {
			var inCost, outCost float64
			tbl.Find("tr").Each(func(_ int, row *goquery.Selection) {
				cells := row.Find("th,td")
				label := strings.ToLower(strings.Join(strings.Fields(cells.First().Text()), " "))
				// Paid tier is col[2]; col[0]=label, col[1]=free tier.
				paidCell := strings.TrimSpace(cells.Eq(2).Text())
				switch {
				case strings.HasPrefix(label, "input price"):
					inCost = parseDollarAmount(paidCell)
				case strings.HasPrefix(label, "output price"):
					outCost = parseDollarAmount(paidCell)
				}
			})
			if inCost > 0 || outCost > 0 {
				ps = pricingSet{inputCost: inCost, outputCost: outCost, found: true}
				return false // stop at first valid table (Standard, not Batch)
			}
			return true
		})

		if ps.found && allowGeminiPricingID(modelID) {
			if _, exists := out[modelID]; !exists {
				out[modelID] = ps
			}
		}
	})
	return out, nil
}

// extractMistralPricing parses https://mistral.ai/pricing.
// The page embeds JSON fragments in script tags with the pattern:
//
//	"<modelAlias>","price":[{"value":"Input (/M tokens)","price_dollar":"$$N.NN",...},...]
//
// Alias-to-versioned-ID mapping is performed so registry keys stay consistent.
func extractMistralPricing(client *http.Client, pageURL string) (map[string]pricingSet, error) {
	body, err := fetchDocumentRaw(client, pageURL, "agent-sdk-go-model-registry-sync/1.0")
	if err != nil {
		return nil, err
	}

	// Pattern: <alias>","price":[{..."Input..."price_dollar":"$$N"...},{..."Output..."price_dollar":"$$N"...}]
	// We match the alias and then the two price_dollar values that follow.
	aliasRe := regexp.MustCompile(`"((?:mistral|magistral|ministral|pixtral|codestral|devstral)[a-z0-9_-]*)","price":\[`)
	priceRe := regexp.MustCompile(`"price_dollar":"(\$?\$?[\d.]+)"`)

	raw := string(body)
	out := map[string]pricingSet{}

	for _, loc := range aliasRe.FindAllStringIndex(raw, -1) {
		m := aliasRe.FindStringSubmatch(raw[loc[0]:loc[1]])
		if len(m) < 2 {
			continue
		}
		alias := m[1]

		// Extract up to 2 price_dollar values in the next 400 chars.
		window := raw[loc[1]:min(loc[1]+400, len(raw))]
		priceMatches := priceRe.FindAllStringSubmatch(window, 2)
		if len(priceMatches) < 2 {
			continue
		}
		inStr := strings.TrimLeft(priceMatches[0][1], "$")
		outStr := strings.TrimLeft(priceMatches[1][1], "$")
		inCost, err1 := strconv.ParseFloat(inStr, 64)
		outCost, err2 := strconv.ParseFloat(outStr, 64)
		if err1 != nil || err2 != nil || (inCost == 0 && outCost == 0) {
			continue
		}

		// Map alias to versioned ID; fall back to alias if not mapped.
		modelID := mistralAliasToVersioned(alias)
		if !allowMistralPricingID(modelID) {
			continue
		}
		if _, exists := out[modelID]; !exists {
			out[modelID] = pricingSet{inputCost: inCost, outputCost: outCost, found: true}
		}
	}
	return out, nil
}

// mistralAliasToVersioned maps Mistral *-latest aliases to their current versioned
// API IDs. When Mistral ships a new version, update the mapping and run the sync.
// Aliases not in this table are passed through unchanged.
func mistralAliasToVersioned(alias string) string {
	const table = `
		mistral-large-latest     mistral-large-2512
		mistral-medium-latest    mistral-medium-2508
		mistral-small-latest     mistral-small-2506
		magistral-medium-latest  magistral-medium-2509
		magistral-small-latest   magistral-small-2509
		ministral-8b-latest      ministral-8b-2512
		ministral-3b-latest      ministral-3b-2410
	`
	for _, line := range strings.Split(table, "\n") {
		fields := strings.Fields(line)
		if len(fields) == 2 && fields[0] == alias {
			return fields[1]
		}
	}
	return alias
}

// parseDollarAmount extracts the first decimal number following a "$" sign.
// Returns 0 if no valid price is found.
func parseDollarAmount(s string) float64 {
	m := pricePattern.FindStringSubmatch(s)
	if len(m) < 2 {
		return 0
	}
	v, err := strconv.ParseFloat(m[1], 64)
	if err != nil {
		return 0
	}
	return v
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}
func applyCapabilityUpdates(content string, updates capabilityUpdates) (string, error) {
	updated := content
	for _, providerID := range []string{"mistral", "openai", "anthropic", "gemini"} {
		re, ok := capabilitySectionRegexps[providerID]
		if !ok {
			return "", fmt.Errorf("missing section regexp for provider: %s", providerID)
		}
		match := re.FindStringSubmatch(updated)
		if len(match) != 4 {
			return "", fmt.Errorf("provider section not found in capabilities.go: %s", providerID)
		}

		existing := parseCapabilityEntries(match[2])
		for _, provider := range updates.providers {
			if provider.providerID != providerID {
				continue
			}
			for _, model := range provider.models {
				existing[model.modelID] = model.caps
			}
		}

		rewrittenBody := renderSortedCapabilityEntries(existing)
		replacement := match[1] + rewrittenBody + match[3]
		updated = strings.Replace(updated, match[0], replacement, 1)
	}
	return updated, nil
}

func parseCapabilityEntries(sectionBody string) map[string]capabilitySet {
	entries := map[string]capabilitySet{}
	matches := capabilityEntryRegexp.FindAllStringSubmatch(sectionBody, -1)
	for _, m := range matches {
		if len(m) != 3 {
			continue
		}
		capsText := m[2]
		entries[m[1]] = capabilitySet{
			vision:    strings.Contains(capsText, "CapabilityVision: true"),
			documents: strings.Contains(capsText, "CapabilityDocuments: true"),
		}
	}
	return entries
}

func renderSortedCapabilityEntries(entries map[string]capabilitySet) string {
	prefixes := make([]string, 0, len(entries))
	for prefix := range entries {
		prefixes = append(prefixes, prefix)
	}
	sort.Strings(prefixes)

	var b strings.Builder
	for _, prefix := range prefixes {
		caps := entries[prefix]
		b.WriteString("\t\t{prefix: ")
		fmt.Fprintf(&b, "%q", prefix)
		b.WriteString(", caps: map[Capability]bool{")
		if caps.vision {
			b.WriteString("CapabilityVision: true")
			if caps.documents {
				b.WriteString(", ")
			}
		}
		if caps.documents {
			b.WriteString("CapabilityDocuments: true")
		}
		b.WriteString("}},\n")
	}
	return b.String()
}

func applyPricingUpdates(content string, updates pricingUpdates) (string, error) {
	updated := content
	scrapedByProvider := map[string]map[string]pricingSet{}
	for _, provider := range updates.providers {
		entries := map[string]pricingSet{}
		for _, model := range provider.models {
			entries[model.modelID] = model.pricing
		}
		scrapedByProvider[provider.providerID] = entries
	}

	for _, providerID := range []string{"anthropic", "gemini", "mistral", "openai"} {
		re, ok := pricingSectionRegexps[providerID]
		if !ok {
			return "", fmt.Errorf("missing section regexp for provider: %s", providerID)
		}
		match := re.FindStringSubmatch(updated)
		if len(match) != 4 {
			return "", fmt.Errorf("provider section not found in pricing.go: %s", providerID)
		}

		existing := parsePricingEntries(match[2])
		if scraped, ok := scrapedByProvider[providerID]; ok && len(scraped) > 0 {
			existing = scraped
		}

		rewrittenBody := renderSortedPricingEntries(providerID, existing)
		replacement := match[1] + rewrittenBody + match[3]
		updated = strings.Replace(updated, match[0], replacement, 1)
	}
	return updated, nil
}

func parsePricingEntries(sectionBody string) map[string]pricingSet {
	entries := map[string]pricingSet{}
	matches := pricingEntryRegexp.FindAllStringSubmatch(sectionBody, -1)
	for _, m := range matches {
		if len(m) == 4 {
			in, _ := strconv.ParseFloat(m[2], 64)
			out, _ := strconv.ParseFloat(m[3], 64)
			entries[m[1]] = pricingSet{inputCost: in, outputCost: out, found: in != 0 || out != 0}
		}
	}
	return entries
}

func renderSortedPricingEntries(providerID string, entries map[string]pricingSet) string {
	modelIDs := make([]string, 0, len(entries))
	for id := range entries {
		modelIDs = append(modelIDs, id)
	}
	sort.Strings(modelIDs)

	var b strings.Builder
	for _, modelID := range modelIDs {
		ps := entries[modelID]
		if ps.inputCost == 0 && ps.outputCost == 0 {
			continue
		}
		b.WriteString(fmt.Sprintf(
			"\t\t%q: {InputCostPerMillion: %s, OutputCostPerMillion: %s},\n",
			modelID,
			formatFloat(ps.inputCost),
			formatFloat(ps.outputCost),
		))
	}
	return b.String()
}

func formatFloat(f float64) string {
	if f == math.Trunc(f) {
		return fmt.Sprintf("%.1f", f)
	}
	return strconv.FormatFloat(f, 'f', -1, 64)
}

