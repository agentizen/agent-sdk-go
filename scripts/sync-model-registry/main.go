type capabilityCollector struct {
	providers map[string]map[string]capabilitySet
}

func newCapabilityCollector() *capabilityCollector {
	return &capabilityCollector{providers: map[string]map[string]capabilitySet{}}
}

func (c *capabilityCollector) add(providerID, modelID string, caps capabilitySet) {
	if _, ok := c.providers[providerID]; !ok {
		c.providers[providerID] = map[string]capabilitySet{}
	}
	c.providers[providerID][modelID] = caps
}

func (c *capabilityCollector) finalize() capabilityUpdates {
	providerIDs := make([]string, 0, len(c.providers))
	for providerID := range c.providers {
		providerIDs = append(providerIDs, providerID)
	}
	sort.Strings(providerIDs)

	out := capabilityUpdates{providers: make([]capabilityProviderUpdate, 0, len(providerIDs))}
	for _, providerID := range providerIDs {
		models := c.providers[providerID]
		modelIDs := make([]string, 0, len(models))
		for modelID := range models {
			modelIDs = append(modelIDs, modelID)
		}
		sort.Strings(modelIDs)

		providerUpdate := capabilityProviderUpdate{
			providerID: providerID,
			models:     make([]capabilityModelUpdate, 0, len(modelIDs)),
		}
		for _, modelID := range modelIDs {
			providerUpdate.models = append(providerUpdate.models, capabilityModelUpdate{
				modelID: modelID,
				caps:    models[modelID],
			})
		}
		out.providers = append(out.providers, providerUpdate)
	}
	return out
}

