func fetchDocumentCorpus(client *http.Client, sourceURL, userAgent string, opts corpusOptions) (string, error) {
	body, err := fetchDocumentRaw(client, sourceURL, userAgent)
	if err != nil {
		return "", err
	}
	return extractDocumentCorpus(body, opts)
}

func fetchDocumentRaw(client *http.Client, sourceURL, userAgent string) ([]byte, error) {
	req, err := http.NewRequest(http.MethodGet, sourceURL, nil)
	if err != nil {
		return nil, err
	}
	req.Header.Set("User-Agent", userAgent)
	req.Header.Set("Accept", "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8")
	req.Header.Set("Accept-Language", "en-US,en;q=0.9")
	req.Header.Set("Cache-Control", "no-cache")
	req.Header.Set("Pragma", "no-cache")

	resp, err := client.Do(req)
	if err != nil {
		return nil, err
	}
	defer func() {
		_ = resp.Body.Close()
	}()

	if resp.StatusCode < 200 || resp.StatusCode >= 300 {
		return nil, fmt.Errorf("unexpected status: %d", resp.StatusCode)
	}

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}
	return body, nil
}

func extractDocumentCorpus(raw []byte, opts corpusOptions) (string, error) {
	corpus, _, err := extractDocumentCorpusAndLinks(raw, opts)
	return corpus, err
}

func extractDocumentCorpusAndLinks(raw []byte, opts corpusOptions) (string, []string, error) {
	doc, err := goquery.NewDocumentFromReader(bytes.NewReader(raw))
	if err != nil {
		return "", nil, err
	}

	var b strings.Builder
	links := make([]string, 0)
	body := doc.Find("body")
	if body.Length() == 0 {
		body = doc.Selection
	}

	b.WriteString(body.Text())
	b.WriteString(" ")

	body.Find("code, pre, kbd, samp, td, th").Each(func(_ int, s *goquery.Selection) {
		b.WriteString(s.Text())
		b.WriteString(" ")
	})

	if opts.includeLinks {
		body.Find("a").Each(func(_ int, s *goquery.Selection) {
			if href, ok := s.Attr("href"); ok {
				b.WriteString(href)
				b.WriteString(" ")
				links = append(links, href)
			}
		})
	}

	lower := strings.ToLower(b.String())
	return spaceRegexp.ReplaceAllString(lower, " "), links, nil
}

func resolveAbsoluteURL(baseURL, ref string) (string, error) {
	base, err := url.Parse(baseURL)
	if err != nil {
		return "", err
	}
	rel, err := url.Parse(strings.TrimSpace(ref))
	if err != nil {
		return "", err
	}
	return base.ResolveReference(rel).String(), nil
}

func extractModelIDs(content string, prefixes []string, allowFn func(string) bool, rules normalizeRules) []string {
	set := map[string]struct{}{}
	for _, token := range strings.Fields(content) {
		candidate := normalizeModelID(token, rules)
		if candidate == "" {
			continue
		}
		if !hasAnyPrefix(candidate, prefixes) {
			continue
		}
		if !allowFn(candidate) {
			continue
		}
		set[candidate] = struct{}{}
	}

	out := make([]string, 0, len(set))
	for id := range set {
		out = append(out, id)
	}
	sort.Strings(out)
	return out
}

func hasAnyPrefix(value string, prefixes []string) bool {
	for _, prefix := range prefixes {
		if strings.HasPrefix(value, prefix) {
			return true
		}
	}
	return false
}

